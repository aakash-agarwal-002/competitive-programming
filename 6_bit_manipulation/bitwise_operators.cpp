#include<iostream>
using namespace std;

int main(){
    int a = 3; // 00000000 00000000 00000000 00000011
    int b = 5; // 00000000 00000000 00000000 00000101

    // Bitwise AND
    cout << "a & b: " << (a & b) << endl; // 00000000 00000000 00000000 00000001 = 1
    // Bitwise OR
    cout << "a | b: " << (a | b) << endl; // 00000000 00000000 00000000 00000111 = 7
    // Bitwise XOR
    cout << "a ^ b: " << (a ^ b) << endl; // 00000000 00000000 00000000 00000110 = 6
    // Bitwise NOT
    cout << "~a: " << (~a) << endl;  // 00000000 00000000 00000000 00000011 => 1(sign)1111111 11111111 11111111 11111100(Magnitude 2's complement) = 1(sign)+0000000 00000000 00000000 00000100(Magnitude) = -4   

    // Left shift a << b = a*2^b
    cout << "a << 1: " << (a << 1) << endl;  //00000000 00000000 00000000 00000011 => 00000000 00000000 00000000 00000110 = 6
    
    // Right shift a >> b = a/2^b
    cout << "a >> 1: " << (a >> 1) << endl; // 00000000 00000000 00000000 00000011 => 00000000 00000000 00000000 00000001 = 1

    
    return 0;   
}